package livraria.ifma.edu.livrariaweb.dto;

public class LivroDTO {

    private Integer id;
    private String nome;

    public LivroDTO() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
